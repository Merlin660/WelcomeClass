package com.wangml.MyPro02;

/**
 * 字符串运算符
 */
public class TestOperator02 {
    public static void main(String[] args) {
        String a = "3"; //字符串
        int b = 4;
        int c = 5;
        char d = 'a'; //字符
        System.out.println(a+b);
        System.out.println(a+c);
        System.out.println(d+4);
    }
}
